@app.route('/register', methods=["GET", "POST"])
def register():
    form = RegisterForm()
    if form.validate_on_submit():
        if User.query.filter_by(email=request.form.get('email')).first():
            flash("You've already signed up with that email, please login instead!")
            return redirect(url_for('login'))

        hash_with_salt = generate_password_hash(request.form["password"], method='pbkdf2:sha256', salt_length=8)
        new_user = User(
            name = request.form["name"],
            email = request.form["email"],
            password = hash_with_salt
        )
        db.session.add(new_user)
        db.session.commit() 
        login_user(new_user)
        return redirect(url_for('aboutme'))

    return render_template("register.html", form=form, logged_in=current_user.is_authenticated)


@app.route('/login', methods=["GET", "POST"])
def login():
    form = LoginForm()
    if request.method == "POST":
        email = request.form.get('email')
        password = request.form.get('password')
      
        # Find user object by email entered
        user = User.query.filter_by(email=email).first()
        # Email doesn't exist
        if not user:
            flash("That email does not exist, please try again.")
            return redirect(url_for('login'))
        # Check stored procedure hash against entered password hashed
        elif not check_password_hash(user.password, password):
            flash("Password incorrect, please try again.")
            return redirect(url_for('login'))
        else:
            login_user(user)
            return redirect(url_for('aboutme', user_id=user.id))

    return render_template("login.html", form=form, logged_in=current_user.is_authenticated)


